#define _CRT_SECURE_NO_WARNINGS  // 보안 경고 무시를 위한 매크로 선언
#include <stdio.h>             // 표준 입출력 함수 포함
#include <string.h>            // 문자열 관련 함수 포함

// 문자열의 역순을 만드는 함수
// p: 원본 문자열의 시작 주소
// q: 역순 문자열을 저장할 배열의 시작 주소
void reverse(char* p, char* q)
{
    int len = strlen(p);  // 원본 문자열의 길이를 계산
    p += len - 1;         // p 포인터를 문자열의 마지막 문자 위치로 이동

    // 원본 문자열의 각 문자를 역순으로 복사하는 루프
    while (len > 0)
    {
        *q = *p;  // 현재 p가 가리키는 문자를 q가 가리키는 위치에 복사
        p--;      // p 포인터를 왼쪽(앞쪽)으로 이동
        q++;      // q 포인터를 오른쪽(다음 위치)로 이동
        len--;    // 남은 문자 수를 1 감소
    }
    *q = '\0';  // 역순 문자열의 끝에 널 종료 문자 추가
}

int main(void)
{
    char s1[100], s2[100], * p, * q;  // s1: 원본 문자열, s2: 역순 문자열, p와 q: 포인터 변수
    int i;  // (추후 확장을 위해 남겨둔 변수, 현재 사용되지 않음)

    printf("문자열 입력: ");
    scanf("%s", s1);  // 사용자로부터 문자열 입력 (공백 없는 문자열)

    p = s1;  // p는 원본 문자열 s1의 시작 주소를 가리킴
    q = s2;  // q는 역순 문자열을 저장할 s2 배열의 시작 주소를 가리킴

    reverse(p, q);  // reverse 함수를 호출하여 s1의 문자열을 역순으로 s2에 저장

    printf("역순문자열: %s \n", s2);  // 역순으로 저장된 문자열 출력

    return 0;  // 프로그램 종료
}