#include <stdio.h>
#include <stdlib.h>
#include <time.h>

// main 함수: 프로그램 시작점
void main()
{
    // 100개의 정수를 저장할 배열 a 선언
    int a[100];
    // 10개의 정수를 저장할 배열 b 선언
    int b[10];
    // 필요에 따른 변수 n 선언 (현재는 사용하지 않음)
    int n = 0;

    // 랜덤 숫자를 저장할 임시 변수 num 선언
    int num;

    // 현재 시간을 기반으로 랜덤 숫자 생성 시드 설정
    srand(time(0));

    // 배열 a에 0부터 100 사이의 랜덤 숫자 100개 저장
    for (int i = 0; i < 100; i++) {
        // 0부터 100까지의 랜덤 숫자 생성
        num = rand() % 101;
        // 생성한 랜덤 숫자를 배열 a의 i번째 위치에 저장
        a[i] = num;
    }

    // 배열 a의 처음 10개 요소를 배열 b에 복사 후 출력
    for (int i = 0; i < 10; i++) {
        b[i] = a[i];          // a 배열의 i번째 값을 b 배열에 복사
        printf("%d ", b[i]);    // 복사한 값을 출력
    }
    // 보기 좋게 두 줄 바꿈
    printf("\n\n");

    // 배열 b에서 최댓값을 찾기 위한 변수 max 선언 및 초기화
    int max = -99999; // 비교 연산을 위해 매우 작은 값으로 초기화

    // 배열 b의 요소를 순회하며 최대값 찾기 (배열의 두 번째 요소부터 시작)
    for (int i = 1; i < 10; i++) {
        if (max < b[i]) {
            max = b[i]; // 현재 값이 max보다 크면 max 값 업데이트
        }
    }
    // 최댓값 출력
    printf("%d ", max);
}